#!/bin/bash

echo -e "\nThis is a clean-up script generated by nebulder (https://github.com/erykjj/nebulder)\nfor automating the removal of a Nebula network device under Linux with systemd\n"

if [ "$(id -u)" != "0" ]; then
 echo -e "* Because of the high-level access required to execute these commands,\n  this script needs to be run a root\n"
 exit 1
fi

echo "* Cleaning up systemd: /etc/systemd/system/nebula_@@tun_device@@.service"
systemctl stop nebula_@@tun_device@@.service
echo "  Service stopped"
systemctl disable nebula_@@tun_device@@.service 
echo "  Service disabled"
rm /etc/systemd/system/nebula_@@tun_device@@.service
systemctl daemon-reload
echo -e "  Unit file removed\n"

echo "* Removing config and keys from /etc/nebula/@@tun_device@@"
rm -rf /etc/nebula/@@tun_device@@
echo -e "  Files removed\n"

echo -e "You may also need to check your firewall rules and remove the nebula binary from /usr/bin\n"

echo -e "Done. If there were no errors, you can remove this script\n"

exit 0